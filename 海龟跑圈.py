import turtle
t = turtle.Turtle()
t.hideturtle()
t.penup()
t.goto(-50,-50)
t.pendown()
t.width(5)
t.color("green")
t.forward(100)
t.circle(50,180)
t.forward(100)
t.circle(50,180)
t1 = turtle.Turtle()
t1.shape("turtle")
t1.penup()
t1.goto(-50,-50)
t1.speed(0)
a = input("请输入海龟跑多少圈:")
a = int(a)
for x in range(a):
    print("当前是第"+str(x+1)+"圈")
    t1.forward(100)
    t1.circle(50,180)
    t1.forward(100)
    t1.circle(50,180)
